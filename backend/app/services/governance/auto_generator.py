class ContextAttachmentGenerator:
    """
    Auto-attach ADRs, specs, and design docs to PR description.

    Algorithm:
    1. Extract modules from changed files
    2. Find ADRs mentioning those modules
    3. Find specs in related directories
    4. Format context section for PR

    Target Latency: <5s
    Time Saved: ~5 minutes per PR
    """

    def __init__(self, repository_path: Optional[str] = None):
        """
        Initialize context attachment generator.

        Args:
            repository_path: Path to repository root
        """
        self.repository_path = repository_path or "."
        self.adr_paths = [
            "docs/02-design/03-ADRs",
            "docs/adr",
            "adr",
        ]
        self.spec_paths = [
            "docs/02-design/specs",
            "docs/01-planning/specs",
            "specs",
        ]

    async def generate(self, pr: PRContext) -> GenerationResult:
        """
        Generate context attachment for a PR.

        Args:
            pr: PR context with changed files

        Returns:
            GenerationResult with context section markdown
        """
        start_time = time.time()

        # Step 1: Extract modules from changed files
        modules = self._extract_modules(pr.changed_files)

        # Step 2: Find relevant ADRs
        adrs = await self._find_relevant_adrs(modules)

        # Step 3: Find relevant specs
        specs = await self._find_relevant_specs(modules)

        # Step 4: Format context section
        context_section = self._format_context_section(
            modules=modules,
            adrs=adrs,
            specs=specs,
            pr_number=pr.pr_number,
        )

        return GenerationResult(
            success=True,
            fallback_level=FallbackLevel.TEMPLATE,  # Always deterministic
            content=context_section,
            confidence=75 if (adrs or specs) else 50,
            latency_ms=(time.time() - start_time) * 1000,
            generator_type=GeneratorType.CONTEXT,
            metadata={
                "pr_number": pr.pr_number,
                "modules": modules,
                "adrs_found": len(adrs),
                "specs_found": len(specs),
            },
        )

    def _extract_modules(self, changed_files: list[str]) -> list[str]:
        """Extract module names from changed file paths."""
        modules = set()

        module_patterns = [
            # backend/app/services/{module}/*.py
            (r"backend/app/services/([^/]+)/", 1),
            # backend/app/api/routes/{module}.py
            (r"backend/app/api/routes/([^/]+)\.py", 1),
            # backend/app/models/{module}.py
            (r"backend/app/models/([^/]+)\.py", 1),
            # frontend/src/components/{module}/
            (r"frontend/src/components/([^/]+)/", 1),
            # frontend/src/app/{module}/
            (r"frontend/src/app/([^/]+)/", 1),
        ]

        import re

        for file_path in changed_files:
            for pattern, group in module_patterns:
                match = re.search(pattern, file_path)
                if match:
                    module = match.group(group)
                    if module not in ["__init__", "__pycache__"]:
                        modules.add(module)

            # Also add parent directory as module
            parts = Path(file_path).parts
            if len(parts) >= 2:
                modules.add(parts[-2])

        return list(modules)[:10]  # Limit to 10 modules

    async def _find_relevant_adrs(
        self, modules: list[str]
    ) -> list[dict[str, str]]:
        """Find ADRs that mention the given modules."""
        adrs = []

        for adr_base in self.adr_paths:
            adr_path = Path(self.repository_path) / adr_base
            if not adr_path.exists():
                continue

            for adr_file in adr_path.glob("*.md"):
                try:
                    content = adr_file.read_text()
                    title = self._extract_title(content)

                    # Check if ADR mentions any module
                    content_lower = content.lower()
                    for module in modules:
                        if module.lower() in content_lower:
                            adrs.append({
                                "file": str(adr_file.relative_to(self.repository_path)),
                                "title": title,
                                "module_match": module,
                            })
                            break

                except Exception as e:
                    logger.debug(f"Error reading ADR {adr_file}: {e}")

        return adrs[:5]  # Limit to 5 ADRs

    async def _find_relevant_specs(
        self, modules: list[str]
    ) -> list[dict[str, str]]:
        """Find specs related to the given modules."""
        specs = []

        for spec_base in self.spec_paths:
            spec_path = Path(self.repository_path) / spec_base
            if not spec_path.exists():
                continue

            for spec_file in spec_path.glob("*.md"):
                try:
                    content = spec_file.read_text()
                    title = self._extract_title(content)
                    filename = spec_file.stem.lower()

                    # Check if spec matches any module
                    for module in modules:
                        if module.lower() in filename or module.lower() in content.lower():
                            specs.append({
                                "file": str(spec_file.relative_to(self.repository_path)),
                                "title": title,
                                "module_match": module,
                            })
                            break

                except Exception as e:
                    logger.debug(f"Error reading spec {spec_file}: {e}")

        return specs[:5]  # Limit to 5 specs

    def _extract_title(self, content: str) -> str:
        """Extract title from markdown content."""
        for line in content.split("\n"):
            line = line.strip()
            if line.startswith("# "):
                return line[2:].strip()
        return "Untitled"

    def _format_context_section(
        self,
        modules: list[str],
        adrs: list[dict[str, str]],
        specs: list[dict[str, str]],
        pr_number: int,
    ) -> str:
        """Format context section for PR description."""
        sections = ["## Related Context (Auto-Generated)", ""]

        # ADRs section
        sections.append("### Architecture Decisions")
        if adrs:
            for adr in adrs:
                sections.append(f"- [{adr['title']}]({adr['file']})")
        else:
            sections.append("- *No related ADRs found*")
        sections.append("")

        # Specs section
        sections.append("### Design Specs")
        if specs:
            for spec in specs:
                sections.append(f"- [{spec['title']}]({spec['file']})")
        else:
            sections.append("- *No related specs found*")
        sections.append("")

        # Modules section
        sections.append("### Affected Modules")
        if modules:
            sections.append(f"- {', '.join(modules)}")
        else:
            sections.append("- *No modules detected*")
        sections.append("")

        sections.append("---")
        sections.append("*Context attached by SDLC Orchestrator Auto-Generation Layer*")

        return "\n".join(sections)


