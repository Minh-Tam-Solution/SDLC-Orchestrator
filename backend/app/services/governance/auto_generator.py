class AutoGenerationService:
    """
    Orchestrates all 4 compliance artifact generators.

    CORE PRINCIPLE:
    "GOVERNANCE MUST BE THE FASTEST WAY"
    Target: <5 minutes per PR (80% faster than 30 min baseline)

    Generators:
    1. IntentGenerator: Task description -> Intent document
    2. OwnershipGenerator: File path -> @owner annotation
    3. ContextAttachmentGenerator: Changed files -> ADR/spec links
    4. AttestationGenerator: AI session -> Attestation template

    Fail-Safe Chain:
    LLM -> Template -> Minimal Placeholder
    Guarantee: Developer can proceed within 2 minutes
    """

    def __init__(
        self,
        ollama_service: Optional[OllamaService] = None,
        repository_path: Optional[str] = None,
    ):
        """
        Initialize auto-generation service.

        Args:
            ollama_service: Ollama service for LLM generation
            repository_path: Path to repository root
        """
        self.ollama = ollama_service or get_ollama_service()
        self.repository_path = repository_path or "."

        # Initialize generators
        self.intent_generator = IntentGenerator(self.ollama)
        self.ownership_generator = OwnershipGenerator(self.repository_path)
        self.context_generator = ContextAttachmentGenerator(self.repository_path)
        self.attestation_generator = AttestationGenerator()

        logger.info("AutoGenerationService initialized")

    async def generate_intent(self, task: TaskContext) -> GenerationResult:
        """
        Generate intent document for a task.

        Args:
            task: Task context

        Returns:
            GenerationResult with intent markdown
        """
        logger.info(f"Generating intent for task: {task.task_id}")
        result = await self.intent_generator.generate(task)
        logger.info(
            f"Intent generated: level={result.fallback_level.value}, "
            f"confidence={result.confidence}, latency={result.latency_ms:.0f}ms"
        )
        return result

    async def suggest_ownership(self, file: FileContext) -> GenerationResult:
        """
        Suggest ownership for a file.

        Args:
            file: File context

        Returns:
            GenerationResult with ownership annotation
        """
        logger.info(f"Suggesting ownership for file: {file.file_path}")
        result = await self.ownership_generator.generate(file)
        logger.info(
            f"Ownership suggested: owner={result.metadata.get('owner')}, "
            f"confidence={result.confidence}, latency={result.latency_ms:.0f}ms"
        )
        return result

    async def attach_context(self, pr: PRContext) -> GenerationResult:
        """
        Attach context (ADRs, specs) to PR.

        Args:
            pr: PR context

        Returns:
            GenerationResult with context section markdown
        """
        logger.info(f"Attaching context for PR: #{pr.pr_number}")
        result = await self.context_generator.generate(pr)
        logger.info(
            f"Context attached: adrs={result.metadata.get('adrs_found')}, "
            f"specs={result.metadata.get('specs_found')}, latency={result.latency_ms:.0f}ms"
        )
        return result

    async def generate_attestation(
        self,
        pr: PRContext,
        ai_session: AISessionContext,
    ) -> GenerationResult:
        """
        Generate attestation template for AI-generated code.

        Args:
            pr: PR context
            ai_session: AI session context

        Returns:
            GenerationResult with attestation markdown
        """
        logger.info(f"Generating attestation for PR: #{pr.pr_number}")
        result = await self.attestation_generator.generate(pr, ai_session)
        logger.info(
            f"Attestation generated: lines={ai_session.generated_lines}, "
            f"min_review={result.metadata.get('min_review_time_minutes'):.1f}min, "
            f"latency={result.latency_ms:.0f}ms"
        )
        return result

    async def generate_all_for_pr(
        self,
        pr: PRContext,
        task: Optional[TaskContext] = None,
        ai_session: Optional[AISessionContext] = None,
    ) -> dict[str, GenerationResult]:
        """
        Generate all applicable compliance artifacts for a PR.

        Args:
            pr: PR context
            task: Optional task context for intent generation
            ai_session: Optional AI session for attestation

        Returns:
            Dict mapping generator type to result
        """
        results = {}

        # Always generate context attachment
        results["context"] = await self.attach_context(pr)

        # Generate intent if task provided
        if task:
            results["intent"] = await self.generate_intent(task)

        # Generate ownership for changed files
        ownership_results = []
        for file_path in pr.changed_files[:5]:  # Limit to 5 files
            ext = Path(file_path).suffix
            file = FileContext(
                file_path=file_path,
                repository_path=pr.repository_path,
                file_extension=ext,
                is_new_file=False,
            )
            ownership_results.append(await self.suggest_ownership(file))
        results["ownership"] = ownership_results

        # Generate attestation if AI session provided
        if ai_session:
            results["attestation"] = await self.generate_attestation(pr, ai_session)

        return results

    def health_check(self) -> dict[str, Any]:
        """
        Check health of auto-generation service.

        Returns:
            Health status dict
        """
        ollama_health = self.ollama.health_check()

        return {
            "service": "AutoGenerationService",
            "healthy": True,
            "ollama_available": ollama_health.get("healthy", False),
            "ollama_models": ollama_health.get("models", []),
            "generators": {
                "intent": "enabled",
                "ownership": "enabled",
                "context": "enabled",
                "attestation": "enabled",
            },
            "fail_safe_enabled": True,
            "target_latency": "<5s per generator",
        }


