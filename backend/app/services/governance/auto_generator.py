def create_auto_generation_service(
    ollama_service: Optional[OllamaService] = None,
    repository_path: Optional[str] = None,
) -> AutoGenerationService:
    """
    Factory function to create AutoGenerationService instance.

    Args:
        ollama_service: Ollama service for LLM generation
        repository_path: Path to repository root

    Returns:
        AutoGenerationService instance
    """
    return AutoGenerationService(
        ollama_service=ollama_service,
        repository_path=repository_path,
    )


# Global instance (lazy initialization)
_auto_generation_service: Optional[AutoGenerationService] = None


def get_auto_generation_service() -> AutoGenerationService:
    """
    Get global AutoGenerationService instance (singleton pattern).

    Returns:
        AutoGenerationService instance
    """
    global _auto_generation_service
    if _auto_generation_service is None:
        _auto_generation_service = create_auto_generation_service()
    return _auto_generation_service
