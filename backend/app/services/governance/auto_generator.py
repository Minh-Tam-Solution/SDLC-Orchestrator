class IntentGenerator:
    """
    Generate intent document from task description.

    Implements 3-level fallback:
    1. LLM (Ollama qwen3:32b) - Best quality
    2. Template - Deterministic
    3. Minimal - Basic structure

    Target Latency: <10s
    Time Saved: ~15 minutes per task
    """

    def __init__(self, ollama_service: Optional[OllamaService] = None):
        """
        Initialize intent generator.

        Args:
            ollama_service: Ollama service for LLM generation
        """
        self.ollama = ollama_service or get_ollama_service()
        self.llm_timeout = 10
        self.llm_model = "qwen3:32b"

    async def generate(self, task: TaskContext) -> GenerationResult:
        """
        Generate intent document for a task.

        Args:
            task: Task context with title, description, acceptance criteria

        Returns:
            GenerationResult with intent document content
        """
        start_time = time.time()

        # Try Level 1: LLM Generation
        try:
            if self.ollama.is_available:
                result = await self._generate_with_llm(task)
                if result.success:
                    result.latency_ms = (time.time() - start_time) * 1000
                    return result
        except Exception as e:
            logger.warning(f"Intent LLM generation failed: {e}")

        # Try Level 2: Template Generation
        try:
            result = self._generate_with_template(task)
            result.latency_ms = (time.time() - start_time) * 1000
            return result
        except Exception as e:
            logger.warning(f"Intent template generation failed: {e}")

        # Level 3: Minimal Fallback (always succeeds)
        result = self._generate_minimal(task)
        result.latency_ms = (time.time() - start_time) * 1000
        return result

    async def _generate_with_llm(self, task: TaskContext) -> GenerationResult:
        """Generate intent using Ollama LLM."""
        prompt = f"""Generate an intent document for this development task.

Task Title: {task.title}
Task Description: {task.description}
Acceptance Criteria: {task.acceptance_criteria or 'Not specified'}
Project: {task.project_name or 'Unknown'}

Create a structured intent document with these exact sections:

# Intent: [Title based on task]

## Why This Change?
[1-2 sentences explaining the business reason, not technical details]

## What Problem Does It Solve?
[2-3 bullet points describing user pain points or system issues]

## Alternatives Considered
[2-3 alternatives that were considered and rejected, with brief reasons]

Output only the markdown content, no additional commentary."""

        try:
            response = self.ollama.generate(
                prompt=prompt,
                model=self.llm_model,
                temperature=0.3,
                max_tokens=1500,
            )

            content = response.response.strip()

            # Validate the response has expected structure
            if "## Why This Change?" in content and "## What Problem" in content:
                return GenerationResult(
                    success=True,
                    fallback_level=FallbackLevel.LLM,
                    content=content,
                    confidence=85,
                    latency_ms=response.total_duration_ms,
                    generator_type=GeneratorType.INTENT,
                    metadata={
                        "model": response.model,
                        "tokens": response.eval_count,
                        "task_id": task.task_id,
                    },
                )

            # LLM response doesn't have expected structure
            logger.warning("LLM response missing expected sections, falling back")
            raise ValueError("LLM response missing expected sections")

        except (OllamaError, ValueError) as e:
            logger.warning(f"LLM intent generation failed: {e}")
            raise

    def _generate_with_template(self, task: TaskContext) -> GenerationResult:
        """Generate intent using template substitution."""
        template = f"""# Intent: {task.title}

## Why This Change?
This change addresses the following requirement:
{task.description[:200]}{'...' if len(task.description) > 200 else ''}

## What Problem Does It Solve?
Based on the task description:
- Primary issue: {task.description.split('.')[0] if task.description else 'Not specified'}
- Impact: Users/system affected by this issue
- Priority: Determined by business requirements

## Alternatives Considered
1. **Do Nothing** - Not viable because the issue impacts operations
2. **Manual Workaround** - Not scalable for production use
3. **This Implementation** - Chosen for balance of effort and impact

---
*Generated from template. Please review and enhance with specific details.*
"""
        return GenerationResult(
            success=True,
            fallback_level=FallbackLevel.TEMPLATE,
            content=template,
            confidence=60,
            latency_ms=0,
            generator_type=GeneratorType.INTENT,
            metadata={"task_id": task.task_id, "source": "template"},
        )

    def _generate_minimal(self, task: TaskContext) -> GenerationResult:
        """Generate minimal intent placeholder."""
        minimal = f"""# Intent: {task.title}

## Why This Change?
[Auto-generation failed. Please document the business reason.]

## What Problem Does It Solve?
{task.description or '[Please describe the problem]'}

## Alternatives Considered
[Please document alternatives you considered.]

---
*Auto-generation failed. Manual input required.*
"""
        return GenerationResult(
            success=True,
            fallback_level=FallbackLevel.MINIMAL,
            content=minimal,
            confidence=30,
            latency_ms=0,
            generator_type=GeneratorType.INTENT,
            metadata={"task_id": task.task_id, "source": "minimal_fallback"},
        )


