class FallbackLevel(str, Enum):
    """
    Fallback level indicates how the content was generated.

    Level 1 (LLM): Best quality, AI-generated
    Level 2 (Template): Good quality, deterministic template
    Level 3 (Minimal): Basic structure, requires manual editing
    """

    LLM = "llm"
    TEMPLATE = "template"
    MINIMAL = "minimal"


class GeneratorType(str, Enum):
    """Types of compliance artifact generators."""

    INTENT = "intent_skeleton"
    OWNERSHIP = "ownership_suggestion"
    CONTEXT = "context_attachment"
    ATTESTATION = "attestation_template"


@dataclass
class GenerationResult:
    """
    Result from any auto-generation operation.

    Attributes:
        success: Whether generation completed (any level)
        fallback_level: Which fallback level was used
        content: Generated content (markdown, annotation, etc.)
        confidence: Confidence score (0-100)
        latency_ms: Generation time in milliseconds
        generator_type: Which generator produced this result
        metadata: Additional context-specific metadata
        error: Error message if generation failed entirely
    """

    success: bool
    fallback_level: FallbackLevel
    content: str
    confidence: int
    latency_ms: float
    generator_type: GeneratorType
    metadata: dict[str, Any] = field(default_factory=dict)
    error: Optional[str] = None

    @property
    def ui_badge(self) -> dict[str, str]:
        """
        Return UI badge based on fallback level.

        Returns:
            Dict with 'color' and 'text' for UI display
        """
        badges = {
            FallbackLevel.LLM: {
                "color": "green",
                "text": "Auto-generated by AI",
            },
            FallbackLevel.TEMPLATE: {
                "color": "yellow",
                "text": "Generated from template. Please review.",
            },
            FallbackLevel.MINIMAL: {
                "color": "orange",
                "text": "Auto-generation failed. Manual input required.",
            },
        }
        return badges.get(self.fallback_level, badges[FallbackLevel.MINIMAL])


