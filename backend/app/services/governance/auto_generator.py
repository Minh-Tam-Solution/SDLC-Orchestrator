class OwnershipGenerator:
    """
    Suggest file ownership based on multiple sources.

    Algorithm (priority order):
    1. CODEOWNERS file (confidence: 1.0)
    2. Directory patterns (confidence: 0.9)
    3. Git blame (confidence: 0.7)
    4. Task creator (confidence: 0.5)
    5. Extension fallback (confidence: 0.3)

    Target Latency: <2s
    Time Saved: ~2 minutes per file
    """

    DIRECTORY_PATTERNS = {
        "backend/app/services/": ("@backend-team", "services"),
        "backend/app/api/": ("@backend-team", "api"),
        "backend/app/models/": ("@backend-team", "models"),
        "backend/app/services/governance/": ("@governance-team", "governance"),
        "frontend/src/components/": ("@frontend-team", "components"),
        "frontend/src/pages/": ("@frontend-team", "pages"),
        "frontend/src/app/": ("@frontend-team", "app"),
        "docs/": ("@documentation-team", "documentation"),
        "tests/": ("@qa-team", "testing"),
    }

    EXTENSION_FALLBACKS = {
        ".py": "@backend-team",
        ".ts": "@frontend-team",
        ".tsx": "@frontend-team",
        ".md": "@documentation-team",
        ".yaml": "@devops-team",
        ".yml": "@devops-team",
        ".sql": "@backend-team",
    }

    def __init__(self, repository_path: Optional[str] = None):
        """
        Initialize ownership generator.

        Args:
            repository_path: Path to repository root for git operations
        """
        self.repository_path = repository_path or "."

    async def generate(self, file: FileContext) -> GenerationResult:
        """
        Suggest ownership for a file.

        Args:
            file: File context with path and extension

        Returns:
            GenerationResult with ownership annotation
        """
        start_time = time.time()
        suggestions = []

        # Source 1: CODEOWNERS
        codeowners_owner = self._check_codeowners(file.file_path)
        if codeowners_owner:
            suggestions.append({
                "owner": codeowners_owner,
                "confidence": 1.0,
                "source": "CODEOWNERS",
            })

        # Source 2: Directory patterns
        pattern_result = self._check_directory_patterns(file.file_path)
        if pattern_result:
            suggestions.append({
                "owner": pattern_result[0],
                "module": pattern_result[1],
                "confidence": 0.9,
                "source": "directory_pattern",
            })

        # Source 3: Git blame (only for existing files)
        if not file.is_new_file:
            git_owner = await self._check_git_blame(file.file_path, file.repository_path)
            if git_owner:
                suggestions.append({
                    "owner": git_owner,
                    "confidence": 0.7,
                    "source": "git_blame",
                })

        # Source 4: Task creator
        if file.task_creator:
            suggestions.append({
                "owner": f"@{file.task_creator}",
                "confidence": 0.5,
                "source": "task_creator",
            })

        # Source 5: Extension fallback
        ext_owner = self.EXTENSION_FALLBACKS.get(file.file_extension)
        if ext_owner:
            suggestions.append({
                "owner": ext_owner,
                "confidence": 0.3,
                "source": "extension_fallback",
            })

        # Select best suggestion
        if not suggestions:
            return GenerationResult(
                success=True,
                fallback_level=FallbackLevel.MINIMAL,
                content=self._format_annotation(
                    owner="@team-lead",
                    module="unknown",
                    file_extension=file.file_extension,
                ),
                confidence=10,
                latency_ms=(time.time() - start_time) * 1000,
                generator_type=GeneratorType.OWNERSHIP,
                metadata={"file_path": file.file_path, "source": "default"},
            )

        # Pick highest confidence
        best = max(suggestions, key=lambda s: s["confidence"])

        # Determine module
        module = best.get("module")
        if not module:
            for pattern, (_, mod) in self.DIRECTORY_PATTERNS.items():
                if pattern in file.file_path:
                    module = mod
                    break
            if not module:
                module = Path(file.file_path).parent.name or "root"

        annotation = self._format_annotation(
            owner=best["owner"],
            module=module,
            file_extension=file.file_extension,
        )

        return GenerationResult(
            success=True,
            fallback_level=FallbackLevel.TEMPLATE,  # Ownership is always deterministic
            content=annotation,
            confidence=int(best["confidence"] * 100),
            latency_ms=(time.time() - start_time) * 1000,
            generator_type=GeneratorType.OWNERSHIP,
            metadata={
                "file_path": file.file_path,
                "owner": best["owner"],
                "module": module,
                "source": best["source"],
                "all_suggestions": suggestions,
            },
        )

    def _check_codeowners(self, file_path: str) -> Optional[str]:
        """Check CODEOWNERS file for ownership."""
        codeowners_paths = [
            Path(self.repository_path) / "CODEOWNERS",
            Path(self.repository_path) / ".github" / "CODEOWNERS",
            Path(self.repository_path) / "docs" / "CODEOWNERS",
        ]

        for codeowners_path in codeowners_paths:
            if codeowners_path.exists():
                try:
                    content = codeowners_path.read_text()
                    for line in content.strip().split("\n"):
                        line = line.strip()
                        if line and not line.startswith("#"):
                            parts = line.split()
                            if len(parts) >= 2:
                                pattern, owner = parts[0], parts[1]
                                if self._matches_pattern(file_path, pattern):
                                    return owner
                except Exception as e:
                    logger.warning(f"Error reading CODEOWNERS: {e}")

        return None

    def _matches_pattern(self, file_path: str, pattern: str) -> bool:
        """Check if file path matches CODEOWNERS pattern."""
        # Simple pattern matching (can be enhanced with fnmatch)
        if pattern.endswith("*"):
            return file_path.startswith(pattern[:-1])
        return file_path == pattern or file_path.endswith(pattern)

    def _check_directory_patterns(
        self, file_path: str
    ) -> Optional[tuple[str, str]]:
        """Check directory patterns for ownership."""
        for pattern, (owner, module) in self.DIRECTORY_PATTERNS.items():
            if pattern in file_path:
                return (owner, module)
        return None

    async def _check_git_blame(
        self, file_path: str, repo_path: str
    ) -> Optional[str]:
        """Get most frequent committer from git blame."""
        try:
            # Run git blame to get committers
            result = subprocess.run(
                ["git", "log", "--format=%an", "-n", "20", "--", file_path],
                cwd=repo_path,
                capture_output=True,
                text=True,
                timeout=5,
            )

            if result.returncode == 0 and result.stdout.strip():
                committers = result.stdout.strip().split("\n")
                # Get most frequent
                from collections import Counter

                counts = Counter(committers)
                most_common = counts.most_common(1)
                if most_common:
                    return f"@{most_common[0][0].replace(' ', '-').lower()}"

        except Exception as e:
            logger.debug(f"Git blame failed for {file_path}: {e}")

        return None

    def _format_annotation(
        self, owner: str, module: str, file_extension: str
    ) -> str:
        """Format ownership annotation based on file type."""
        date = datetime.now(timezone.utc).strftime("%Y-%m-%d")

        if file_extension in [".py"]:
            return f"""# @owner: {owner}
# @module: {module}
# @created: {date}
"""
        elif file_extension in [".ts", ".tsx", ".js", ".jsx"]:
            return f"""/**
 * @owner {owner}
 * @module {module}
 * @created {date}
 */
"""
        elif file_extension in [".yaml", ".yml"]:
            return f"""# @owner: {owner}
# @module: {module}
# @created: {date}
"""
        else:
            # Default format
            return f"""# @owner: {owner}
# @module: {module}
# @created: {date}
"""


